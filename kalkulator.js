const tambah = (number1, number2) => {
  return number1 + number2;
};

const kurang = (number1, number2) => {
  return number1 - number2;
};

const kali = (number1, number2) => {
  return number1 * number2;
};

const bagi = (number1, number2) => {
  return number1 / number2;
};

module.exports = { tambah, kurang, bagi, kali };
